package Multithreading;
public class ThreadLifeCycle {
    /*
    ---------Simple Life Cycle ----------
    1. new/born state:: whenever new object is created, it is known as new/ born state(Mythread t= new Mythread);)
    2.whenever  start method is called:: thread enters into runnable/ ready state.
    3. if a processor is allocated to thread by thread scheduler, it enters into running state.
    4. once run method is completed, thread enters into dead state
    ----------
    once a life cycle of thread is started , if we try to start it again we'll be getting runtime exception (IllegalThreadState exception)
     */
}
