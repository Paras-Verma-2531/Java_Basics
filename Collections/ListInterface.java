package Collections;

public class ListInterface {
    /*
    List is a child interface of Collection
    whenever we are supposed to add elements where duplicates are allowed and order of insertion is preserved. then we should
    go for List.
    ======================
    methods of List are:
    1. void add(int index,object o);
    2.boolean addAll(int index,collection c);
    3.Object get(int index);
    4.Object remove(int index);
    5. Object set(int index,object new);// sets new element at that location
    6.int indexOf(object o)// returns first occurrence
    7.int lastIndexOf(object o);
    ======================================
    Various implementation classes of List are:
    1. ArrayList_imp
    2.LinkedList
    3.Stack
      |.Vector

     */
}
